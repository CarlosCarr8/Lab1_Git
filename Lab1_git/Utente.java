
public class Utente {
	
	private String nome;
	private String genero;
	private String idade;
	private float altura;
	private int peso;
	
	public Utente(String nome, String genero, String idade, float altura, int peso) {
		super();
		this.nome = nome;
		this.genero = genero;
		this.idade = idade;
		this.altura = altura;
		this.peso = peso;
	}

}
